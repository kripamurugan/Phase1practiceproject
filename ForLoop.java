package corejavapracticeproject;

public class ForLoop {

	public static void main(String[] args) {
		// for loop
		System.out.println("Counting from 1 to 5 using a for loop:");

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println("For loop finished!");
	}

}
